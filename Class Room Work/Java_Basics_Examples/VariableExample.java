public class VariableExample {
    public static void main(String[] args) {
        int age = 20;
        double marks = 89.5;
        char grade = 'A';
        String name = "Ravi";

        System.out.println("Name: " + name);
        System.out.println("Age: " + age);
        System.out.println("Marks: " + marks);
        System.out.println("Grade: " + grade);
    }
}
