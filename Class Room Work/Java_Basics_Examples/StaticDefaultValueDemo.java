public class StaticDefaultValueDemo {
    static int intValue;
    static double doubleValue;
    static boolean boolValue;
    static String textValue;

    public static void main(String[] args) {
        System.out.println("Default int: " + intValue);
        System.out.println("Default double: " + doubleValue);
        System.out.println("Default boolean: " + boolValue);
        System.out.println("Default String: " + textValue);
    }
}
